    /**
     * Recebe um elemento JTable e o popula com os dados do banco.
     *
     * @param jTabela JTable a ser populado
     */
    public void preencherTabela(JTable jTabela) {

        List<Turno> turnos = TurnosController.getTurnos();

        DefaultTableModel dtm = (DefaultTableModel) jTabela.getModel();

        dtm.setRowCount(turnos.size());

        jTabela.setModel(dtm);

        int posicaoLinha = 0;

        for (int i = 0; i < turnos.size(); i++) {
            jTabela.setValueAt(turnos.get(i).getNomeFuncionario(), posicaoLinha, 0);
            jTabela.setValueAt(turnos.get(i).getCargo(), posicaoLinha, 1);
            jTabela.setValueAt(turnos.get(i).getDataInicial().format(dtf), posicaoLinha, 2);
            jTabela.setValueAt(turnos.get(i).getDataFinal().format(dtf), posicaoLinha, 3);
            jTabela.setValueAt(turnos.get(i).getDataFinal().plusHours(36).format(dtf), posicaoLinha, 4);
            posicaoLinha += 1;
        }
    }

